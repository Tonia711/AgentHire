drop policy if exists "Business sees own record" on public.businesses;
create policy "Business sees own record"
on public.businesses
for all
using (auth.role() = 'service_role' or user_id = auth.uid())
with check (auth.role() = 'service_role' or user_id = auth.uid());

drop policy if exists "Business sees own contractors" on public.contractors;
create policy "Business sees own contractors"
on public.contractors
for all
using (
  auth.role() = 'service_role'
  or business_id in (select b.id from public.businesses b where b.user_id = auth.uid())
)
with check (
  auth.role() = 'service_role'
  or business_id in (select b.id from public.businesses b where b.user_id = auth.uid())
);

drop policy if exists "Business sees own invoices" on public.invoices;
create policy "Business sees own invoices"
on public.invoices
for all
using (
  auth.role() = 'service_role'
  or business_id in (select b.id from public.businesses b where b.user_id = auth.uid())
)
with check (
  auth.role() = 'service_role'
  or business_id in (select b.id from public.businesses b where b.user_id = auth.uid())
);

drop policy if exists "Business sees own attestations" on public.attestations;
create policy "Business sees own attestations"
on public.attestations
for all
using (
  auth.role() = 'service_role'
  or contractor_id in (
    select c.id
    from public.contractors c
    where c.business_id in (select b.id from public.businesses b where b.user_id = auth.uid())
  )
)
with check (
  auth.role() = 'service_role'
  or contractor_id in (
    select c.id
    from public.contractors c
    where c.business_id in (select b.id from public.businesses b where b.user_id = auth.uid())
  )
);

drop policy if exists "Public read access to app status" on public.app_status;
create policy "Public read access to app status"
on public.app_status
for select
using (true);

