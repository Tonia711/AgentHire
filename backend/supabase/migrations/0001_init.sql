create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.businesses (
  id uuid primary key default gen_random_uuid(),
  user_id uuid unique references auth.users(id) on delete set null,
  name text not null,
  wallet_address text unique,
  chain_id bigint not null default 43113,
  created_at timestamptz not null default now()
);

create table if not exists public.contractors (
  id uuid primary key default gen_random_uuid(),
  business_id uuid not null references public.businesses(id) on delete cascade,
  name text not null,
  email text not null,
  hourly_rate numeric(10,2) not null check (hourly_rate >= 0),
  contract_terms text not null default 'standard',
  wallet_address text,
  preferred_chain_id bigint default 43113,
  preferred_token text default 'dNZD',
  status public.contractor_status not null default 'invited',
  civic_pass_id text,
  invite_token uuid not null default gen_random_uuid(),
  gst_registered boolean not null default false,
  ir330c_uploaded boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.invoices (
  id uuid primary key default gen_random_uuid(),
  onchain_id bigint,
  business_id uuid not null references public.businesses(id) on delete cascade,
  contractor_id uuid not null references public.contractors(id) on delete cascade,
  amount numeric(10,2) not null check (amount >= 0),
  gst_amount numeric(10,2) not null default 0 check (gst_amount >= 0),
  token_symbol text not null default 'dNZD',
  chain_id bigint not null default 43113,
  description text,
  status public.invoice_status not null default 'created',
  invoice_tx_hash text,
  tx_hash text,
  created_at timestamptz not null default now(),
  paid_at timestamptz,
  unique (chain_id, onchain_id)
);

create table if not exists public.attestations (
  id uuid primary key default gen_random_uuid(),
  contractor_id uuid not null references public.contractors(id) on delete cascade,
  attestation_uid text not null unique,
  schema_uid text,
  attestation_type public.attestation_type not null,
  document_hash text,
  tx_hash text,
  chain_id bigint not null default 43113,
  created_at timestamptz not null default now()
);

create table if not exists public.app_status (
  id bigint generated by default as identity primary key,
  name text not null unique,
  value text not null,
  updated_at timestamptz not null default now()
);

drop trigger if exists trg_contractors_updated_at on public.contractors;
create trigger trg_contractors_updated_at
before update on public.contractors
for each row execute function public.set_updated_at();

create index if not exists idx_businesses_user_id on public.businesses(user_id);
create index if not exists idx_contractors_business_id on public.contractors(business_id);
create index if not exists idx_contractors_invite_token on public.contractors(invite_token);
create index if not exists idx_invoices_business_id on public.invoices(business_id);
create index if not exists idx_invoices_contractor_id on public.invoices(contractor_id);
create index if not exists idx_attestations_contractor_id on public.attestations(contractor_id);

